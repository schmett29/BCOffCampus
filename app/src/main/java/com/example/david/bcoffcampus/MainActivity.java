    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_gallery) {
            pager.setCurrentItem(1);
        } else if (id == R.id.nav_slideshow) {
            pager.setCurrentItem(0);
        } else if (id == R.id.nav_share) {
            String url = "http://davidschmetterling.com";
            Intent i = new Intent(Intent.ACTION_VIEW);
            i.setData(Uri.parse(url));
            startActivity(i);
        } else if (id == R.id.nav_send) {
            Intent intent = new Intent(Intent.ACTION_SEND);
            intent.setType("message/rfc822");
            intent.putExtra(Intent.EXTRA_SUBJECT, "Hot new app");
            intent.putExtra(Intent.EXTRA_TEXT, "Wow, I found this great new app to find off campus housing at BC. It's creator, David Schmetterling is a genius!");

            startActivity(Intent.createChooser(intent, "Send Email"));

        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }

